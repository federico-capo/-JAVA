package Esercizi;

public class Persona {
	
	private String nome;
	private String cognome;
	
	
	public String getNome() {
		return nome;
	}
	public void setNome(String n) {
		this.nome = n;
	}
	public String getCognome() {
		return cognome;
	}
	public void setCognome(String c) {
		this.cognome = c;
	}
	
	
	
}
